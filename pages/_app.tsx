// Create a context for the DuckDB instance
const DuckDBContext = createContext<any>(null);

// Custom hook to use the DuckDB context
export const useDuckDB = () => useContext(DuckDBContext);

const App = (props: React.PropsWithChildren) => {
  const { data: session, status } = useSession();
  const router = useRouter();
  const { to } = router.query;
  const fetchRuntimeConfig = useAppStore((state) => state.fetchRuntimeConfig);
  const runtimeConfig = useAppStore((state) => state.runtimeConfig);
  const { activeSession, activeApplication, activeTask, colorScheme } =
    useAppStore();
  const go = useGo();

  const [dbInstance, setDbInstance] = useState<any>(null);

  useEffect(() => {
    // Initialize DuckDB instance once
    const initializeDB = async () => {
      const db = await initializeLocalDB();
      setDbInstance(db);
    };

    initializeDB();

    // Save the database state to local storage on page unload
    window.addEventListener("beforeunload", saveDatabaseToLocalStorage);

    // Clean up event listener on component unmount
    return () => {
      window.removeEventListener("beforeunload", saveDatabaseToLocalStorage);
    };
  }, []);

  useEffect(() => {
    fetchRuntimeConfig();
  }, [fetchRuntimeConfig]);

  if (status === "loading" || !runtimeConfig) {
    return <span>loading...</span>;
  }

  let API_URL = runtimeConfig?.API_URL;

  // Function to return the access token
  const getToken = () => {
    return session?.token?.account?.access_token || ""; // Adjust this based on your session structure
  };
  const getStateIds = () => {
    let stateIds: { [key: string]: any } = {};

    if (activeSession?.id) {
      stateIds["session_id"] = activeSession.id;
    }
    if (activeApplication?.id) {
      stateIds["application_id"] = activeApplication.id;
    }
    if (activeTask?.id) {
      stateIds["task_id"] = activeTask.id;
    }
    if (session?.token?.account?.providerAccountId) {
      stateIds["user_id"] = session.token.account.providerAccountId;
    }

    return stateIds;
  };

  const authProvider: AuthBindings = {
    login: async ({ providerName, email, password }) => {
      if (providerName) {
        signIn(providerName, {
          callbackUrl: to ? to.toString() : "/",
          redirect: true,
        });
        return {
          success: true,
        };
      }

      const signInResponse = await signIn("CredentialsSignIn", {
        email,
        password,
        callbackUrl: to ? to.toString() : "/",
        redirect: false,
      });

      if (!signInResponse) {
        return {
          success: false,
        };
      }

      const { ok, error } = signInResponse;

      if (ok) {
        return {
          success: true,
          redirectTo: "/",
        };
      }

      return {
        success: false,
        error: new Error(error?.toString()),
      };
    },

    logout: async () => {
      signOut({
        redirect: true,
        callbackUrl: "/",
      });

      return {
        success: true,
      };
    },
    onError: async (error) => {
      console.error(error);
      return {
        error,
      };
    },
    check: async () => {
      if (status === "unauthenticated") {
        return {
          authenticated: false,
          redirectTo: "/login",
        };
      }

      return {
        authenticated: true,
      };
    },
    getPermissions: async () => {
      return null;
    },
    getIdentity: async () => {
      if (session?.user) {
        const { user } = session;
        return {
          ...user,
          avatar: user?.image || null,
        };
      }

      return null;
    },
  };

  return (
    <>
      <DuckDBContext.Provider value={dbInstance}>
        <MantineProvider
          theme={theme}
          defaultColorScheme={colorScheme?.scheme || "auto"}
        >
          <ContextMenuProvider>
            <Notifications position="top-right" />
            <Refine
              routerProvider={routerProvider}
              dataProvider={{
                default: defaultApiDataProvider(API_URL, getToken, getStateIds),
              }}
              notificationProvider={useNotificationProvider}
              authProvider={authProvider}
              accessControlProvider={accessControlProvider}
              resources={[
                {
                  name: "home",
                  list: "/home",
                },
                {
                  name: "profile",
                  list: "/profile",
                },
                {
                  name: "applications",
                  list: "/applications",
                  create: "/applications/create",
                  edit: "/applications/edit/:id",
                  show: "/applications/show/:id",
                },

                {
                  name: "sessions",
                  list: "/sessions",
                  create: "/:applicationId/sessions/create",
                  edit: "/:applicationId/sessions/:id/edit",
                  show: "/:applicationId/sessions/:sessionId/tasks/:taskId",
                },
                {
                  name: "tasks",
                  list: "/tasks",
                  show: "/tasks/show/:id",
                },
              ]}
              options={{
                syncWithLocation: true,
                warnWhenUnsavedChanges: true,
                projectId: "OpGcqe-gAGTnn-eW9pDg",
              }}
            >
              {/* <div>
            MYAPP //{" "}
            {JSON.stringify(session?.token?.account?.providerAccountId)}
          </div> */}
              {/* <button
            onClick={() =>
              signOut({
                redirect: true,
                callbackUrl: "/login",
              })
            }
          >
            logout
          </button> */}
              {router.pathname === "/login" && <div>{props.children}</div>}
              {router.pathname !== "/login" && (
                <BaseLayout>{props.children}</BaseLayout>
              )}

              {/* <DocumentTitleHandler handler={customTitleHandler} /> */}
              <ReactQueryDevtools initialIsOpen={false} />
            </Refine>
          </ContextMenuProvider>
        </MantineProvider>
      </DuckDBContext.Provider>
    </>
  );
};

function MyApp({
  Component,
  pageProps: { session, ...pageProps },
}: ExtendedAppProps): JSX.Element {
  return (
    <SessionProvider session={session}>
      <App>
        <Component {...pageProps} />
      </App>
    </SessionProvider>
  );
}

export default MyApp;
