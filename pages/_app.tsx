// Ensure that customTitleHandlerProps has the correct types
interface customTitleHandlerProps {
  resource?: IResourceItem;
  action?: Action;
  params?: Record<string, string | undefined>;
  pathname?: string;
  autoGeneratedTitle: string;
}

const customTitleHandler = ({
  resource,
  action,
  params,
  autoGeneratedTitle,
}: customTitleHandlerProps) => {
  const fetchRuntimeConfig = useAppStore((state) => state.fetchRuntimeConfig);
  const runtimeConfig = useAppStore((state) => state.runtimeConfig);

  useEffect(() => {
    fetchRuntimeConfig();
  }, [fetchRuntimeConfig]);

  let title = runtimeConfig?.APP_NAME; // Default title

  if (resource && action) {
    title = `${resource} ${action} ${params?.id}`;
  }

  return title;
};

